     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $client = new Client();
        $response = $client->request('GET', 'http://swapi.co/api/people');
        $body= $response->getBody();  //obtiene el resultado del get
        $content = $body->getContents(); //obtiene el contenido del body
        $JSON_content = json_decode($content, TRUE); //parsea a JSON
        return view('welcome')->with(['people'=>$JSON_content['results']]); //envia el array 'results' el cual contiene los datos de people
    }

